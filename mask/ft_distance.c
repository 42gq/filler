#include "../filler.h"

int	ft_distance(int a, int b, int y, int x)
{
	int	tmp1;
	int	tmp2;

	tmp1 = a - y;
	tmp2 = b - x;
	tmp1 *= (tmp1 < 0) ? -1 : 1;
	tmp2 *= (tmp2 < 0) ? -1 : 1;
	return (tmp1 + tmp2);
}
