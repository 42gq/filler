#include "../filler.h"

void	ft_sum(t_env *e)
{
	int	i;
	int	j;

	i = -1;
	while (++i < e->Y)
	{
		j = -1;
		while (++j < e->X)
		{
			e->mask[i][j].sum = e->mask[i][j].minus + e->mask[i][j].plus;
		}
	}
}
